# simple interest calculation

principal_amount = float(input("please enter the principal_amount: "))
rate_of_interest = float(input("please enter the rate_of_interest: "))
time_period = float(input("please enter the time_period: "))
simple_interest = (principal_amount * rate_of_interest * time_period)/100
print("simple interest: " ,simple_interest)









